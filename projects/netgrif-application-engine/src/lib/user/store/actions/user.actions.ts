import {createAction, props} from '@ngrx/store';
import {User} from "../../models/user";

export enum UserActionTypes {
    SET_USER = '[USER] Login user',
    SET_USER_SUCCESS = '[USER] Login user success',
    SET_USER_FAILURE = '[USER] Login user failure',

    REMOVE_USER = '[USER] Logout user'
}

export const loginUser = createAction(
    UserActionTypes.SET_USER,
    props<{ user: User }>()
);

export const loginSuccess = createAction(
    UserActionTypes.SET_USER_SUCCESS,
    props<{ user: User }>()
);

export const loginFailure = createAction(
    UserActionTypes.SET_USER_FAILURE,
    props<{ error: any }>()
);

export const logoutUser = createAction(
    UserActionTypes.REMOVE_USER,
);
